package ex1;

// Classe représentant des opérations de calcul simples.

public class calcul {

    //Additionne deux nombres entiers.

    public int additionner(int a, int b) {
        return a + b;
    }

    // Soustrait deux nombres entiers.

    public int soustraire(int a, int b) {
        return a - b;
    }
}
